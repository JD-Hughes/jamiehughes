function rotateText() {
    if (textEl.classList.contains("rotate")) textEl.classList.remove("rotate");
    else textEl.classList.add("rotate");
}

function newPrompt() {
    let randomPrompt = Math.floor(Math.random() * prompts[gamemode].length);
    rotateText();
    setTimeout(() => {
        if (prompts[gamemode][randomPrompt]) {
            textEl.innerText = prompts[gamemode][randomPrompt];
            prompts[gamemode].splice(randomPrompt, 1);
            rotateText();
        } else {
            textEl.innerText = "No more questions :(";
            rotateText();
        }
    }, 600);
}

/* View in fullscreen */
function openFullscreen() {
    if (elem.requestFullscreen) {
        elem.requestFullscreen();
    } else if (elem.webkitRequestFullscreen) {
        /* Safari */
        elem.webkitRequestFullscreen();
    } else if (elem.msRequestFullscreen) {
        /* IE11 */
        elem.msRequestFullscreen();
    }
}

//Check if there is a gamemode loaded. Else redirect to main screen
function checkGamemode() {
    const validModes = ["0", "1", "2", "3", "4"];
    if (!validModes.includes(gamemode)) {
        window.location.replace(window.location.href.toString().split("/play")[0]); // Bet you've never seen jank like this 😎
    }
}

